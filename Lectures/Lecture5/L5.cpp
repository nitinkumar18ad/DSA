#include<iostream>
using namespace std;

int main(){
    int a = 4;
    int b = 6;

    cout<<"a&b= "<<(a&b)<<endl;
    cout<<"a|b= "<<(a|b)<<endl;
    cout<<"a^b= "<<(a^b)<<endl;
    cout<<"~a= "<<(~a)<<endl;

    cout<<(17>>2)<<endl;
    cout<<(19>>3)<<endl;
    cout<<(17<<2)<<endl;
    cout<<(19<<4)<<endl;


    int i=7;
    cout<<i++<<endl;
    cout<<++i<<endl;
    cout<<i--<<endl;
    cout<<--i<<endl;

}





